export const getKnowledgeByCategory = (category) => {
  return EDUTECHLIFE_KNOWLEDGE[category] || null;
};

export const searchKnowledge = (query) => {
  const results = [];
  const lowerQuery = query.toLowerCase();

  Object.entries(EDUTECHLIFE_KNOWLEDGE).forEach(([key, value]) => {
    const content = typeof value.content === 'string' 
      ? value.content 
      : JSON.stringify(value.content);
    
    if (content.toLowerCase().includes(lowerQuery)) {
      results.push({
        category: key,
        title: value.title,
        relevance: content.toLowerCase().split(lowerQuery).length - 1
      });
    }

    if (value.tags && value.tags.some(tag => tag.toLowerCase().includes(lowerQuery))) {
      results.push({
        category: key,
        title: value.title,
        relevance: 10
      });
    }
  });

  return results.sort((a, b) => b.relevance - a.relevance);
};

export const formatKnowledgeForPrompt = () => {
  let context = "INFORMACIÓN CORPORATIVA DE EDUTECHLIFE:\n\n";

  Object.entries(EDUTECHLIFE_KNOWLEDGE).forEach(([key, value]) => {
    if (value.sensitive) return;

    context += `## ${value.title}\n`;
    
    if (typeof value.content === 'string') {
      context += `${value.content}\n\n`;
    } else if (Array.isArray(value.content)) {
      value.content.forEach(item => {
        context += `- ${item}\n`;
      });
      context += '\n';
    } else if (typeof value.content === 'object') {
      Object.entries(value.content).forEach(([subKey, subValue]) => {
        context += `- ${subKey}: ${subValue}\n`;
      });
      context += '\n';
    }
  });

  return context;
};

